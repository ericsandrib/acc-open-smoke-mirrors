type SelectContentProps = React.ComponentPropsWithoutRef<typeof SelectPrimitive.Content>

/**
 * Default collision insets for Radix Popper (`flip` / `shift` / `size`).
 * Extra bottom inset reserves space for fixed footers and toolbars so the menu
 * flips above the trigger when vertically constrained; callers can override per field.
 */
const DEFAULT_SELECT_COLLISION_PADDING = { top: 12, right: 12, bottom: 72, left: 12 }

function mergeSelectCollisionPadding(
  prop: SelectContentProps["collisionPadding"],
): { top: number; right: number; bottom: number; left: number } {
  if (prop === undefined) return { ...DEFAULT_SELECT_COLLISION_PADDING }
  if (typeof prop === "number") {
    return {
      ...DEFAULT_SELECT_COLLISION_PADDING,
      top: prop,
      right: prop,
      left: prop,
    }
  }
  return { ...DEFAULT_SELECT_COLLISION_PADDING, ...prop }
}

const SelectContent = React.forwardRef<
  React.ComponentRef<typeof SelectPrimitive.Content>,
  SelectContentProps
>(({ className, children, position = "popper", collisionPadding: collisionPaddingProp, ...props }, ref) => {
  return (
    <SelectPrimitive.Portal>
      <SelectPrimitive.Content
        ref={ref}
        className={cn(
          "relative z-[100] max-h-[min(24rem,var(--radix-select-content-available-height,24rem))] min-w-[8rem] overflow-hidden rounded-md border bg-popover text-popover-foreground shadow-md data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0 data-[state=closed]:zoom-out-95 data-[state=open]:zoom-in-95 data-[side=bottom]:slide-in-from-top-2 data-[side=left]:slide-in-from-right-2 data-[side=right]:slide-in-from-left-2 data-[side=top]:slide-in-from-bottom-2",
          position === "popper" &&
            "data-[side=bottom]:translate-y-1 data-[side=left]:-translate-x-1 data-[side=right]:translate-x-1 data-[side=top]:-translate-y-1",
          className,
        )}
        position={position}
        {...(position === "popper"
          ? { collisionPadding: mergeSelectCollisionPadding(collisionPaddingProp) }
          : {})}
        {...props}
      >
        <SelectScrollUpButton />
        <SelectPrimitive.Viewport
          className={cn(
            "p-1",
            position === "popper" &&
              "h-[var(--radix-select-trigger-height)] w-full min-w-[var(--radix-select-trigger-width)]",
          )}
        >
          {children}
        </SelectPrimitive.Viewport>
        <SelectScrollDownButton />
      </SelectPrimitive.Content>
    </SelectPrimitive.Portal>
  )
})
SelectContent.displayName = SelectPrimitive.Content.displayName

